package com.ddm.cbic.model;

public class ViewData {
	
	String role;
	String nameSecondary;
	String designationSecondary;
	String mobileSecondary;
	String emailSecondary;
	String divName;
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getNameSecondary() {
		return nameSecondary;
	}
	public void setNameSecondary(String nameSecondary) {
		this.nameSecondary = nameSecondary;
	}
	public String getDesignationSecondary() {
		return designationSecondary;
	}
	public void setDesignationSecondary(String designationSecondary) {
		this.designationSecondary = designationSecondary;
	}
	public String getMobileSecondary() {
		return mobileSecondary;
	}
	public void setMobileSecondary(String mobileSecondary) {
		this.mobileSecondary = mobileSecondary;
	}
	public String getEmailSecondary() {
		return emailSecondary;
	}
	public void setEmailSecondary(String emailSecondary) {
		this.emailSecondary = emailSecondary;
	}
	public String getDivName() {
		return divName;
	}
	public void setDivName(String divName) {
		this.divName = divName;
	}
	

}
